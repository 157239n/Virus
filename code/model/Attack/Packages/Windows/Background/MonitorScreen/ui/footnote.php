<h2>Explanation</h2>
<p>This attack is meant to run continuously in the background, and will get the host's screenshot every hour. The data
    is then sent back continuously and is ready to be viewed. There are 2 views: "daily", which has all the screenshots
    in the past 24 hours, and the other, "saved" has all the screenshots that you have intentionally saved. Some
    screenshots are thus shared between 2 views. Because the screenshots get automatically deleted after 24 hours, if
    you wish to take note of an interesting screenshot, you must save it. If you no longer need a saved view, there is
    an option to forget about it. Also for each screenshots, you can assign it a short name so that you can remember it
    easier.</p>
<p>Please note that you must run an easy.Screenshot attack before, and it has to work, before this attack becomes
    effective.</p>
<p>An interesting phenomenon is that sometimes the virus won't report every hour. When the host computer first started
    up, it will report right away and wait for the next hour to report. Thus, if the host computer restarts multiple
    times then there can be multiple reports in an hour.</p>
